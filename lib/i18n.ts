// ── Słowniki ───────────────────────────────────────────────────────────────────

const translations = { pl, en }

// ── Hook ──────────────────────────────────────────────────────────────────────

type Lang = "pl" | "en"
type TranslationKey = string

function getNestedValue(obj: any, path: string): string {
  const parts = path.split(".")
  let current = obj
  for (const part of parts) {
    if (current == null) return path
    current = current[part]
  }
  return typeof current === "string" ? current : path
}

export function useT() {
  const { lang } = useTheme()
  const dict = translations[lang as Lang] ?? translations.pl

  return function t(key: TranslationKey): string {
    return getNestedValue(dict, key) || getNestedValue(translations.pl, key) || key
  }
}

// Wersja bez hooka (do użycia poza komponentami)
export function getT(lang: Lang) {
  const dict = translations[lang] ?? translations.pl
  return function t(key: string): string {
    return getNestedValue(dict, key) || getNestedValue(translations.pl, key) || key
  }
}

export type { Lang, TranslationKey }
export { pl, en }
